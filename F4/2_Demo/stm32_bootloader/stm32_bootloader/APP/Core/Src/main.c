/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	
  /* USER CODE BEGIN 1 */
	SCB->VTOR = FLASH_BASE | 0x10000;//设置偏移量
	RTC_TimeTypeDef RTC_TimeStruct;
  RTC_DateTypeDef RTC_DateStruct;
	uint8_t tbuf[40];
	uint8_t t=0;
	uint8_t key=0;
	u16 oldcount=0;				    //老的串口接收数据值

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_FSMC_Init();
  MX_USART1_UART_Init();
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */
	bsp_systick_init(168);
	bsp_uart1_init();
	drv_LCD_Init();
	LCD_Clear(WHITE);
	POINT_COLOR=RED;//设置字体为红色
  printf("Welcome!!\r\n");	
	LCD_ShowString(30,50,200,16,16,"Explorer STM32F4");	
	LCD_ShowString(30,70,200,16,16,"APP TEST");	
	LCD_ShowString(30,90,200,16,16,"ATOM@ALIENTEK");
	LCD_ShowString(30,110,200,16,16,"2025/1/19");  
	LCD_ShowString(30,130,200,16,16,"KEY_UP:Copy APP2FLASH");
	LCD_ShowString(30,150,200,16,16,"KEY1:LED1 ON");
	LCD_ShowString(30,170,200,16,16,"KEY0:LED1 OFF");
	POINT_COLOR=BLUE;//设置字体为蓝色
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		if(USART_RX_CNT){
			if(oldcount==USART_RX_CNT)//新周期内,没有收到任何数据,认为本次数据接收完成.
			{
				HAL_UART_Transmit(&huart1,(uint8_t*)USART_RX_BUF,USART_RX_CNT,1000);	//发送接收到的数据
				USART_RX_CNT=0;
			}else oldcount=USART_RX_CNT;			
		}
		
		t++;
		if((t%10)==0)	//每100ms更新一次显示数据
		{
      HAL_RTC_GetTime(&hrtc,&RTC_TimeStruct,RTC_FORMAT_BIN);
			sprintf((char*)tbuf,"Time:%02d:%02d:%02d",RTC_TimeStruct.Hours,RTC_TimeStruct.Minutes,RTC_TimeStruct.Seconds); 
			LCD_ShowString(30,190,210,16,16,tbuf);	
            HAL_RTC_GetDate(&hrtc,&RTC_DateStruct,RTC_FORMAT_BIN);
			sprintf((char*)tbuf,"Date:20%02d-%02d-%02d",RTC_DateStruct.Year,RTC_DateStruct.Month,RTC_DateStruct.Date); 
			LCD_ShowString(30,210,210,16,16,tbuf);	
			sprintf((char*)tbuf,"Week:%d",RTC_DateStruct.WeekDay); 
			LCD_ShowString(30,230,210,16,16,tbuf);
		} 
		if((t%20)==0) {LED0(2);}	//每200ms,翻转一次LED0 
        delay_ms(10);
    /* USER CODE END WHILE */
		
    /* USER CODE BEGIN 3 */
		key=Key_Scan(0);
		if(key==KEY0_PRES)	//WK_UP按键按下
		{
			LED1(0);
		}
		if(key==KEY1_PRES)	//WK_UP按键按下
		{
			LED1(1);
		}
		
  }
  /* USER CODE END 3 */
}

